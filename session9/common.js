var x = 5, y = 9;
function multi(p1, p2) {
	return p1 * p2;
}
function devi(p1, p2) {
	return p1 / p2;
}
function plus(p1, p2) {
	return p1 + p2;
}
function sub(p1, p2) {
	return p1 - p2;
}
function mod(p1, p2) {
	return p1 % p2;
}
document.write(multi(x, y), "<br>"); 
document.write(devi(x, y), "<br>");
document.write(plus(x, y), "<br>");
document.write(sub(x, y), "<br>");
document.write(mod(x, y), "<br>");
